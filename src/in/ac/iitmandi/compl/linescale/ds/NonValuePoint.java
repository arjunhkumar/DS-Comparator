package in.ac.iitmandi.compl.linescale.ds;

/**
 * @author arjun
 *
 */
public class NonValuePoint {
	
	public final int x;
	public final int y;
	
	public NonValuePoint() {
		this.x = 0;
		this.y = 0;
	}
	
	public NonValuePoint(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
	
}
